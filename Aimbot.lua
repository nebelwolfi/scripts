local function SortEnemies(pos, mode)
    local enemyHeroes = table.copy(GetEnemyHeroes()) 
    local sort = {
        [1] = function(pos) table.sort(enemyHeroes, function(a,b) return GetDistance(pos,a)<GetDistance(pos,b) end) end,
        [2] = function() table.sort(enemyHeroes, function(a,b) return a.physReduction*a.magicReduction*a.health<b.physReduction*b.magicReduction*b.health end) end,
        [3] = function() table.sort(enemyHeroes, function(a,b) return (a.totalDamage*a.attackSpeed*a.range/1000+a.magicArmor+a.armor+a.ap*(1-a.cdr))*a.level*a.health/a.maxHealth>(b.totalDamage*b.attackSpeed*b.range/1000+b.magicArmor+b.armor+b.ap*(1-b.cdr))*b.level*b.health/b.maxHealth end) end,
        [4] = function() table.sort(enemyHeroes, function(a,b) return a.distance<b.distance end) end,
        [5] = function() table.sort(enemyHeroes, function(a,b) return a.distance>b.distance end) end,
        [6] = function()--[[**FAP**]]end,
    }
    sort[mode](pos)
    return enemyHeroes
end
