local allowCast = {}
-- helper classes S
class "Spell"
function Spell:__init(slot, type, name, range, width, delay, speed, collision, aoe)
	self.slot, self.type, self.name, self.range, self.width, self.delay, self.speed, self.collision, self.aoe = slot, type, name, range, width, delay, speed, collision, aoe
end

function Spell:Cast(target, hitchance, sourcePos)
	if target and GetDistance(target, sourcePos) > (self.range <= 0 and self.width or self.range) then return end
	if self.aoe then
		local pred = ({GetLinearAOEPrediction, GetCircularAOEPrediction, GetConicAOEPrediction})[self.type]
		local p = pred(target, self, sourcePos)
		if p.hitChance >= (hitchance or 0.25) then
            allowCast[self.slot] = true
			myHero:CastSpell(self.slot, p.castPos.x, p.castPos.z)
		end
	else
        local p = GetPrediction(target, self, sourcePos)
        if p.hitChance >= (hitchance or 0.25) and (not self.collision or not p:mCollision(self.collision)) then
            allowCast[self.slot] = true
            myHero:CastSpell(self.slot, p.castPos.x, p.castPos.z)
        end
	end
end

function Spell:Draw(around, color)
	local pos = around or myHero.pos
	DrawCircle3D(pos.x, pos.y, pos.z, self.range <= 0 and self.width or self.range, 1, color or 0xffffffff, 32)
end
