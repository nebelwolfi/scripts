        Config.Spells:DropDown("m"..i, "Target Selector Mode", 1, {"Closest to CastPos", "Fastest Dead", "Highest Threat", "Closest to myHero", "Furthest to myHero", "Random"})
    end
    Config:Boolean("on", "Enabled", true)
    Config:Key("off", "Disabled", 32)
end)
Callback.Add("SpellCast", function(iSpell)
    if allowCast[iSpell.spellID] then
        allowCast[iSpell.spellID] = false
        return
    end
    if mySpells[iSpell.spellID] and Config.Spells["u"..iSpell.spellID]:Value() and EnemiesAround(myHero.pos, mySpells[iSpell.spellID].range) > 0 then 
        lastSpell = table.copy(iSpell)
        lastSpell.time = os.clock()
        BlockCast()
    end
end)
Callback.Add("Tick", function()
    if lastSpell then
        if lastSpell.time+0.125 < os.clock() then
            lastSpell = nil
            return
        end
        local iSpell = lastSpell.spellID
        if EnemiesAround(myHero.pos, mySpells[iSpell].range) == 0 then
            lastSpell = nil
            return
        end
        if myHero:CanUseSpell(iSpell) > 0 then
            lastSpell = nil
            return
        end
        local targets = SortEnemies(lastSpell.endPos, Config.Spells["m"..iSpell]:Value())
        for i=1, #targets do
            local target = targets[i]
            if target and target.valid then
                mySpells[iSpell]:Cast(target, Config.Spells["h"..iSpell]:Value())
            end
        end
    end
end)
Callback.Add("Draw", function()
    for i, v in pairs(mySpells) do
        if Config.Spells["d"..i] and Config.Spells["d"..i]:Value() then
            v:Draw(myHero.pos)
        end
    end
end)
